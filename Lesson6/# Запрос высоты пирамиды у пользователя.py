# Запрос высоты пирамиды у пользователя
высота = int(input("Введите высоту пирамиды: "))

# Цикл для отрисовки пирамиды
for i in range(1, высота + 1):
    # Выводим пробелы для выравнивания слева
    for j in range(высота - i):
        print(" ", end="")
    # Выводим звездочки для текущего уровня пирамиды
    for k in range(2 * i - 1):
        print("*", end="")
    # Переходим на следующую строку для нового уровня
    print()
    